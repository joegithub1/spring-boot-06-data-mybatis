package com.cn.bean;

public class Department {
    private int depaId;
    private String depaName;
    private int depaNo;
    private int isDel;
    private int depaStatus;

    public int getDepaId() {
        return depaId;
    }

    public void setDepaId(int depaId) {
        this.depaId = depaId;
    }

    public String getDepaName() {
        return depaName;
    }

    public void setDepaName(String depaName) {
        this.depaName = depaName;
    }

    public int getDepaNo() {
        return depaNo;
    }

    public void setDepaNo(int depaNo) {
        this.depaNo = depaNo;
    }

    public int getIsDel() {
        return isDel;
    }

    public void setIsDel(int isDel) {
        this.isDel = isDel;
    }

    public int getDepaStatus() {
        return depaStatus;
    }

    public void setDepaStatus(int depaStatus) {
        this.depaStatus = depaStatus;
    }
}
